    public void upDateDB()
    {
        try
        {
            sqlConn = MyConnection.getConnection();
            pst = sqlConn.prepareStatement("select * from add_student");
            
            rs = pst.executeQuery();
            ResultSetMetaData stData=rs.getMetaData();
            
            q=stData.getColumnCount();
            
            DefaultTableModel RecordTable = (DefaultTableModel)jTable1.getModel();
            RecordTable.setRowCount(0);
            
            while(rs.next())
            {
                Vector columnData = new Vector();
                
                for (i=1;i <=q; i++)
                {
                    columnData.add(rs.getString("id"));
                    columnData.add(rs.getString("name"));
                    columnData.add(rs.getString("father_name"));
                    columnData.add(rs.getString("mother_name"));
                    columnData.add(rs.getString("mobile"));
                    columnData.add(rs.getString("email"));
                    columnData.add(rs.getString("nationality"));
                    columnData.add(rs.getString("blood_group"));
                    columnData.add(rs.getString("address"));
                    columnData.add(rs.getString("course"));
                    
                }
                RecordTable.addRow(columnData);
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
