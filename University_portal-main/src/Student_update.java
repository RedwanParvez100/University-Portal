    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        

        try {
            sqlConn = MyConnection.getConnection();
            pst = sqlConn.prepareStatement("select * from add_student");
            
            rs = pst.executeQuery();
            while(rs.next())
            {
                String id=String.valueOf(rs.getInt("id"));
                String name = rs.getString("name");
                //String username = rs.getString("username");
                String father_name = rs.getString("father_name");
                String mother_name = rs.getString("mother_name");
                String mobile = rs.getString("mobile");
                String email = rs.getString("email");
                String nationality = rs.getString("nationality");
                String blood_group = rs.getString("blood_group");
                String address = rs.getString("address");
                String course = rs.getString("course");
                
                String tbData[]={id,name,father_name,mother_name,mobile,email,nationality,blood_group,address,course};
                DefaultTableModel RecordTable = (DefaultTableModel)jTable1.getModel();
                RecordTable.addRow(tbData);
            }
            
            
            sqlConn.close();
        } catch (SQLException ex) {
            Logger.getLogger(Student_update.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

